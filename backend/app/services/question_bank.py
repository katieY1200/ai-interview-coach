def get_fallback_questions(job_category: str, count: int = 2) -> list[str]:
    bank = QUESTION_BANK.get(job_category, QUESTION_BANK["기획"])
    return random.sample(bank, min(count, len(bank)))


def get_personality_questions(company_type: str, count: int = 4) -> list[str]:
    key = "startup" if company_type == "startup" else "large"
    bank = PERSONALITY_QUESTIONS[key]
    return random.sample(bank, min(count, len(bank)))
